    private void showCBox(){
        cboxOrder.removeAllItems();
        OrderList oList = new OrderList(customer.getUsername());
        if(!oList.get().isEmpty()){
            for(Order o : oList.get()){
                VendorClass vc = new VendorClass(o.getVendorid());
                orderList.add(o);
                cboxOrder.addItem(vc.getName());
            }
        }
    }
    
    
    
    private void checkRunnerStatus(){
        String type = order.getOrderType();
        if(type.equals("Delivery")){
            DeliveryOrder o = (DeliveryOrder) order;
            lblLocationWord.setVisible(true);
            lblLocation.setVisible(true);
            lblRunner.setVisible(true);
            lblRunner.setText("Runner: ");
            lblRunnerStatus.setVisible(true);
            lblLocation.setText(o.getLocation());
            
            String status = o.getRunnerStatus();
            DeliveryTask dt = new DeliveryTask(o);
            if(o.getRunnerid() != null || dt.getTaskStatus() != null){
                lblRunnerStatus.setText(o.getRunnerStatus());
                lblRunner.setText(o.getRunnerid());
            }
            else if(status.equals("Waiting for pickup")){
                lblRunnerStatus.setText("Waiting for pickup");
            }
            else if(status.equals("Delivery Complete")){
                lblRunnerStatus.setText("Completed");
            }
        }
        else{
            lblRunnerStatus.setText("Pending");
        }
    }
    
    
    private void checkVendorStatus(){
        String status = order.getOrderStatus();
        System.out.println(order.getOrderStatus());
        if(status.equals("Preparing")){
            lblVendorStatus.setText("Preparing");
        }
        else if(status.equals("Wait to take")){
            lblVendorStatus.setText("Completed");
        }
        else{
            lblVendorStatus.setText("Pending");
        }
    }
    
    private boolean checkCancel(){
        return !lblVendorStatus.equals("Pending") || !lblRunnerStatus.equals("Pending");
    }


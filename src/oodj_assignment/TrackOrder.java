    private void cboxOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboxOrderActionPerformed
        int index = cboxOrder.getSelectedIndex();
        if(index >= 0){
            initialize();
            order = orderList.get(index);
            lblOrderType.setText(order.getOrderType());
            lblTotalPrice.setText(order.getTotalPrice() + "");
            checkVendorStatus();
            checkRunnerStatus();
        }
    }//GEN-LAST:event_cboxOrderActionPerformed

    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        int index = cboxOrder.getSelectedIndex();
        if(checkCancel()){
            order = orderList.get(index);
            order.cancel();
            JOptionPane.showMessageDialog(null, "Order deleted", "Order cancelation", JOptionPane.OK_OPTION);
            this.setVisible(false);
            new TrackOrder(customer).setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(null, "Order is being accepted already");
        }
    }//GEN-LAST:event_btnCancelActionPerformed
    


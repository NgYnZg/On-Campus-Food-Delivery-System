    private void btnCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCartActionPerformed
        row = tableOrderHistory.getSelectedRow();
        String value = model.getValueAt(row, 0).toString();
        String type = model.getValueAt(row, 2).toString();
        Order order;
        if(type.equals("DineIn")){
            order = new DineInOrder(value);
            new OrderDetails(order).setVisible(true);
        }
        else if(type.equals("TakeAway")){
            order = new TakeAwayOrder(value);
            new OrderDetails(order).setVisible(true);
        }
        else if(type.equals("Delivery")){
            order = new DeliveryOrder(value);
            new OrderDetails(order).setVisible(true);
        }
        else
            System.out.println("Not valid order type");
        
        
        
    }//GEN-LAST:event_btnCartActionPerformed



    private void showTable(){
        tableOrderHistory.setModel(model);
        model.setRowCount(0);
        model.setColumnIdentifiers(column);
        OrderList oList = new OrderList(customer.getUsername());
        for(Order o : oList.get()){
            Cart c = o.getCart();
            VendorClass vc = new VendorClass(c.getVendorid());
            String[] rowData = {o.getOrderid(), vc.getName(), o.getOrderType(), o.getTotalPrice() + "", o.getOrderStatus(), o.getDateTime()};
            model.addRow(rowData);
        }
        tableOrderHistory.removeColumn(tableOrderHistory.getColumnModel().getColumn(0));
    }
    

